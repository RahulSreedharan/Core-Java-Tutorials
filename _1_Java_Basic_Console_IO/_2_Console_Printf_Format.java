/**
	java console operations using System.out.printf();

*/

import java.util.*;
import java.lang.*;

class _2_Console_Printf_Format
{
	public static void main(String[] Args)
	{
		int    i = 255;
		double d = 3.142857142857143;
		String s = "Hello from Java";
		
		System.out.printf("int    i = %d \n",i);
		System.out.printf("double d = %f \n",d);
		System.out.printf("string s = %s \n",s);
	}
}